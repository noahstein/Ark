#include "Sse.h"


/*========================================================================
  Code
========================================================================*/
namespace ark::hal::simd
{
	/*--------------------------------------------------------------------
	  SIMD architecture tag indicating he CPU has SSE2 capabilities.
	--------------------------------------------------------------------*/
	class Sse2 : public Sse
	{
	};


	/*--------------------------------------------------------------------
	  Concept to restrict templates to CPUs with the SSE2 ISA.
	--------------------------------------------------------------------*/
	template<typename SIMD>
	concept IsSse2 = IsSse<SIMD> && std::is_base_of_v<Sse2, SIMD>;
}


