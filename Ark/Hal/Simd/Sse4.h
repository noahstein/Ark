namespace ark::hal::simd
{
	/*--------------------------------------------------------------------
	  SIMD architecture tag indicating he CPU has SSE4 capabilities.
	--------------------------------------------------------------------*/
	class Sse4 : public Sse3
	{
	};


	/*--------------------------------------------------------------------
	  Concept to restrict templates to CPUs with the SSE3 ISA.
	--------------------------------------------------------------------*/
	template<typename SIMD>
	concept IsSse4 = IsSse3<SIMD> && std::is_base_of_v<Sse4, SIMD>;
}


