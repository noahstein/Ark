namespace ark::hal::simd
{
	/*--------------------------------------------------------------------
	  SIMD architecture tag indicating he CPU has AVX2 capabilities.
	--------------------------------------------------------------------*/
	class Avx2 : public Avx
	{
	};


	/*--------------------------------------------------------------------
	  Concept to restrict templates to CPUs with the AVX2 ISA.
	--------------------------------------------------------------------*/
	template<typename SIMD>
	concept IsAvx2 = IsAvx<SIMD> && std::is_base_of_v<Avx2, SIMD>;
}


